package com.olajideosho.fastandroidnetworkingexample;

public class AdvertObject {
    public String company;
    public String url;
    public String text;

    public AdvertObject() {
    }

    public String getCompany() {
        return company;
    }

    public String getUrl() {
        return url;
    }

    public String getText() {
        return text;
    }
}
